package remy.auricoste.tondeuse.tondeuse.exception;

/**
 * Commentaire
 *
 * @version $Revision$ $Date$
 */
public class TondeusePositionException extends Exception {

    public TondeusePositionException() {
    }

    public TondeusePositionException(Throwable cause) {
        super(cause);
    }

    public TondeusePositionException(String message) {
        super(message);
    }

    public TondeusePositionException(String message, Throwable cause) {
        super(message, cause);
    }

    public TondeusePositionException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
